class lec2 {
    // Variables 
    public static void main(String args[]){
        // Primitive 
        // Integer 
        int a = 10;
        int b = 20;
        int res = a + b;
        System.out.println(res);
        byte age = 34;
        short age2 = 56;
        long ageDino = 5666666666666666666L;
        System.out.println(ageDino);

        // Float
        float marks = 10.2f;
        System.out.println(marks);
        
        // double 
        double marks2 = 10.2;
        System.out.println(marks2);

        // char
        char c = 'k';
        System.out.println(c);

        // boolean
        boolean b1 = true;
        System.out.println(b1);

        // String
        String str = "Kartik";
        System.out.println(str);

        // literal

        // 1. Binary format Integer Value
         int x = 0b1110;
        System.out.println(x);

        // 2. HexaDecimal 
        int l = 0x7E;
        System.out.println(l);


        // Apart from this to not get confused with number of zeros in large numbers we can use underscore
        int z = 1_00_000;
        System.out.println(z);

        

    }
}
